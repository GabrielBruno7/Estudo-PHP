<?php

//operador de igualdade ==
$frutas = "maçã";
var_dump($frutas == "maçã");


//operador de Diferença !=
$frutas = "maçã";
var_dump($frutas != "maçã");

//operador de Idêntico  ===
$frutas = 1;
var_dump($frutas === 1);

//operador de Não Idêntico  !==
$frutas = 1;
var_dump($frutas !== 1);

//operador de Maior  >
$frutas = 1;
var_dump($frutas > 10);

//operador de Maior ou Igual  >=
$frutas = 1;
var_dump($frutas >= 10);

//operador de Menor <
$frutas = 1;
var_dump($frutas < 10);

//operador de Menor <=
$frutas = 1;
var_dump($frutas <= 10);


?>